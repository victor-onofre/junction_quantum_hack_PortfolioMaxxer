#!/usr/bin/env python3

import numpy as np
from scipy.sparse import csr_matrix

def parse_tsv(tsv_file):
  with open(tsv_file, 'r') as infile:
    line = next(infile).strip()
    n, m = map(int, line.split())
    row_indices = []
    col_indices = []
    targets = []
    for i, line in enumerate(infile):
      line = line.strip().split()
      if line[0] == "-0.5000":
        targets.append([1])
      else:
        assert line[0] == "0.5000"
        targets.append([0])
      for j in line[1:]:
        j = int(j)
        assert i < m, f'too high i = {i}'
        assert j < n, f'too high j = {j}'
        row_indices.append(i)
        col_indices.append(j)

    H = csr_matrix(
      (np.ones(len(row_indices), dtype=int), (row_indices, col_indices)), shape=(m, n))
    return H, targets

def parse_pegpchk(fname):
  # read the graph generated by PEG
  with open(fname, 'r') as f:
    m = int(f.readline().rstrip('\n'))
    n = int(f.readline().rstrip('\n'))
    row_indices = []
    col_indices = []

    # ignore third line which contains number of columns for remaining lines
    _ = f.readline()
    check_num = 0
    for line in f:
      vals = [int(val) for val in line.rstrip('\n').rstrip(' ').split(' ')]
      for val in vals:
        if val != 0:  # 0 is used to denote absence of variable node
          row_indices.append(val - 1)
          col_indices.append(check_num)
      check_num += 1
    assert check_num == n

  H = csr_matrix((np.ones(len(row_indices), dtype=int),
                  (row_indices, col_indices)), shape=(m, n))
  return H

def parse_alist(alist_file):
  with open(alist_file, 'r') as infile:
    lines = infile.read().strip().split('\n')

  # Parse header
  N, M = map(int, lines[0].split())

  # Initialize sparse matrix
  row_indices = []
  col_indices = []

  # Parse nlist
  nlist_start = 4
  for i in range(N):
    nlist = list(map(int, lines[nlist_start + i].split()))
    for j in nlist:
      if j == 0:
        # NB: If the rows or columns are irregular, you must pad the low-weight
        # rows/columns with zeroes so as to make the two sets of lists regular.
        continue
      row_indices.append(i)
      col_indices.append(j - 1)

  # Create the sparse matrix
  matrix = csr_matrix((np.ones(len(row_indices), dtype=int), (row_indices, col_indices)), shape=(N, M))

  return matrix

def dump_to_alist(matrix, filename):
  N, M = matrix.shape  # Dimensions of the matrix
  num_nlist = np.sum(matrix.astype('bool'), axis=1)  # Number of 1s in each row
  num_mlist = np.sum(matrix.astype('bool'), axis=0)  # Number of 1s in each column

  # Maximum number of 1s in any row and column
  biggest_num_n = int(np.max(num_nlist))
  biggest_num_m = int(np.max(num_mlist))

  # Generating nlist and mlist
  nlist = [[j+1 for j in range(M) if matrix[i, j] == 1] for i in range(N)]
  mlist = [[i+1 for i in range(N) if matrix[i, j] == 1] for j in range(M)]

  # Pad nlist and mlist for regularity
  for row in nlist:
    while len(row) < biggest_num_n:
      row.append(0)
  for col in mlist:
    while len(col) < biggest_num_m:
      col.append(0)

  # Writing to file
  with open(filename, 'w') as file:
    file.write(f"{N} {M}\n")
    file.write(f"{biggest_num_n} {biggest_num_m}\n")
    file.write(' '.join(map(str, num_nlist)) + '\n')
    file.write(' '.join(map(str, num_mlist)) + '\n')
    for row in nlist:
      file.write(' '.join(map(str, row)) + '\n')
    for col in mlist:
      file.write(' '.join(map(str, col)) + '\n')

  recovered = parse_alist(filename)
  assert (recovered == matrix).all()

# tsv
def dump_old_format(H, fname):
  m, n = H.shape
  with open(fname, 'w') as outfile:
    outfile.write(f'{n}\t{m}\n')
    for i in range(m):
      v = np.random.randint(2)
      if v:
        outfile.write('-')
      outfile.write('0.5000')
      for j in H[i].nonzero()[1]:
        outfile.write(f'\t{j}')
      outfile.write('\n')

# csv
def dump_new_format(H, fname, p=2, targets=None):
  m, n = H.shape
  with open(fname, 'w') as outfile:
    outfile.write(f'{n},{m},{p}\n')
    current_size = int(p/2)
    for i in range(m):
      if targets is None:
        acceptable = np.random.choice(np.arange(p), size=current_size, replace=False)
      else:
        acceptable = targets[i]
      if current_size > p/2:
        current_size -= 1
      elif current_size < p/2:
        current_size += 1
      v = ','.join(f'{elem}' for elem in acceptable)
      outfile.write(v)
      for j in H[i].nonzero()[1]:
        outfile.write(f',{j}:{H[i,j]}')
      outfile.write('\n')

def dump_to_dem(H, fname):
  with open(fname, 'w') as outfile:
    for e in range(H.shape[0]):
      estr = ' '.join(f'D{d}' for d in H[e].nonzero()[1])
      outfile.write(f'error(0.2) {estr}\n')
    for d in range(H.shape[1]):
      outfile.write(f'DETECTOR({d}, 0, 0) D{d}\n')

if __name__ == '__main__':
  import argparse
  parser = argparse.ArgumentParser(
    'convert a progressive edge growth (PEG) .code '
    'file to a max-xorsat problem instance tsv file.')
  parser.add_argument('--in', dest='input_fname', type=str, required=True, help='file to load')
  parser.add_argument('--in-format', type=str, required=False, default='peg',
                      help='peg / tsv / csv / alist')
  parser.add_argument('--out', type=str, required=True, help='file name to write to')
  parser.add_argument('--out-format', type=str, required=True, help='tsv / csv / alist / dem')
  args = parser.parse_args()
  if args.in_format not in {'peg', 'alist', 'tsv'}:
    print('No no no, this only supports converting from --in-format peg and alist and tsv')
    exit(1)
  targets = None
  if args.in_format == 'peg':
    H = parse_pegpchk(args.input_fname)
  elif args.in_format == 'tsv':
    H, targets = parse_tsv(args.input_fname)
  else:
    assert args.in_format == 'alist'
    H = parse_alist(args.input_fname)

  if args.out_format == 'tsv':
    dump_old_format(H, args.out)
  elif args.out_format == 'csv':
    dump_new_format(H, args.out, targets=targets)
  elif args.out_format == 'alist':
    dump_to_alist(H, args.out)
  elif args.out_format == 'dem':
    dump_to_dem(H, args.out)
  else:
    raise ValueError(f'invalid format: {args.out_format}')
    
