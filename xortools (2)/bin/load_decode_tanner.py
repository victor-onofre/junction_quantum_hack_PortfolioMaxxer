ell = math.ceil(delta*m)
error = np.zeros(m, dtype=int)
hyperedges = np.random.choice(np.arange(m), size=ell, replace=False)
error[hyperedges] = 1

def nearest_codeword(x):
  return codebook[np.argmin(np.abs(codebook-x.reshape(1,-1)).sum(axis=-1))]


while True != 0:
  flip_votes = np.zeros(m, dtype=int)
  for i in tqdm(range(H.shape[1]), total=H.shape[1]):
    subseq = np.where(H[:,i])[0]
    c = nearest_codeword(error[subseq])
    flip_votes[subseq] += (error[subseq] != c)

  max_votes = flip_votes.max()
  if max_votes == 0:
    break
  idx = flip_votes==max_votes
  weight_change = idx.sum() - 2*error[idx].sum()
  print(f'flipping {sum(idx)} bits, error weight changed by {weight_change}, current error weight {error.sum()+weight_change}')
  error[idx] = 1-error[idx]

